let order = [];
let playerOrder = [];
let flash;
let turn;
let good;
let compTurn;
let intervalId;
let strict = false;
let noise = true;
let on = false;
let win;

const turnCounter = document.querySelector("#turn");
const rectangle1 = document.querySelector("#rectangle1");
const rectangle2 = document.querySelector("#rectangle2");
const rectangle3 = document.querySelector("#rectangle3");
const rectangle4 = document.querySelector("#rectangle4");
const strictButton = document.querySelector("#strict");
const onButton = document.querySelector("#on");
const startButton = document.querySelector("#start");

strictButton.addEventListener('click', (event) => {
  if (strictButton.checked == true) {
    strict = true;
  } else {
    strict = false;
  }
});

onButton.addEventListener('click', (event) => {
  if (onButton.checked == true) {
    on = true;
    turnCounter.innerHTML = "-";
  } else {
    on = false;
    turnCounter.innerHTML = "";
    clearColor();
    clearInterval(intervalId);
  }
});

startButton.addEventListener('click', (event) => {
  if (on || win) {
    play();
  }
});

function play() {
  win = false;
  order = [];
  playerOrder = [];
  flash = 0;
  intervalId = 0;
  turn = 1;
  turnCounter.innerHTML = 1;
  good = true;
  for (var i = 0; i < 20; i++) {
    order.push(Math.floor(Math.random() * 4) + 1);
  }
  compTurn = true;

  intervalId = setInterval(gameTurn, 800);
}

function gameTurn() {
  on = false;

  if (flash == turn) {
    clearInterval(intervalId);
    compTurn = false;
    clearColor();
    on = true;
  }

  if (compTurn) {
    clearColor();
    setTimeout(() => {
      if (order[flash] == 1) one();
      if (order[flash] == 2) two();
      if (order[flash] == 3) three();
      if (order[flash] == 4) four();
      flash++;
    }, 200);
  }
}

function one() {
  if (noise) {
    let audio = document.getElementById("son1");
    audio.play();
  }
  noise = true;
  rectangle1.style.backgroundColor = "yellow";
}

function two() {
  if (noise) {
    let audio = document.getElementById("son2");
    audio.play();
  }
  noise = true;
  rectangle2.style.backgroundColor = "red";
}

function three() {
  if (noise) {
    let audio = document.getElementById("son3");
    audio.play();
  }
  noise = true;
  rectangle3.style.backgroundColor = "blue";
}

function four() {
  if (noise) {
    let audio = document.getElementById("son4");
    audio.play();
  }
  noise = true;
  rectangle4.style.backgroundColor = "green";
}

function clearColor() {
  rectangle1.style.backgroundColor = "goldenrod";
  rectangle2.style.backgroundColor = "darkred";
  rectangle3.style.backgroundColor = "darkblue";
  rectangle4.style.backgroundColor = "darkgreen";
}

function flashColor() {
  rectangle1.style.backgroundColor = "yellow";
  rectangle2.style.backgroundColor = "red";
  rectangle3.style.backgroundColor = "blue";
  rectangle4.style.backgroundColor = "green";
}

rectangle1.addEventListener('click', (event) => {
  if (on) {
    playerOrder.push(1);
    check();
    one();
    if(!win) {
      setTimeout(() => {
        clearColor();
      }, 300);
    }
  }
})

rectangle2.addEventListener('click', (event) => {
  if (on) {
    playerOrder.push(2);
    check();
    two();
    if(!win) {
      setTimeout(() => {
        clearColor();
      }, 300);
    }
  }
})

rectangle3.addEventListener('click', (event) => {
  if (on) {
    playerOrder.push(3);
    check();
    three();
    if(!win) {
      setTimeout(() => {
        clearColor();
      }, 300);
    }
  }
})

rectangle4.addEventListener('click', (event) => {
  if (on) {
    playerOrder.push(4);
    check();
    four();
    if(!win) {
      setTimeout(() => {
        clearColor();
      }, 300);
    }
  }
})

function check() {
  if (playerOrder[playerOrder.length - 1] !== order[playerOrder.length - 1])
    good = false;

  if (playerOrder.length == 3 && good) {
    winGame();
  }

  if (good == false) {
    flashColor();
    turnCounter.innerHTML = "Dommage, tu peux recommencer";
    setTimeout(() => {
      turnCounter.innerHTML = turn;
      clearColor();

      if (strict) {
        play();
      } else {
        compTurn = true;
        flash = 0;
        playerOrder = [];
        good = true;
        intervalId = setInterval(gameTurn, 800);
      }
    }, 800);

    noise = false;
  }

  if (turn == playerOrder.length && good && !win) {
    turn++;
    playerOrder = [];
    compTurn = true;
    flash = 0;
    turnCounter.innerHTML = turn;
    intervalId = setInterval(gameTurn, 800);
  }

}

function winGame() {
  flashColor();
  turnCounter.innerHTML = "Tu as gagn√©, bravo";
  on = false;
  win = true;
}





